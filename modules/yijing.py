yijing_traits = {i + 1: ["流動", "轉變", "內省"] if i % 8 == 0 else ["穩定", "成長", "突破"] for i in range(64)}

def get_yijing_hexagram(year: int, month: int, day: int) -> dict:
    total = year + month + day
    index = total % 64
    return {
        "hexagram_index": index + 1,
        "hexagram_name": yijing_names[index],
        "traits": yijing_traits.get(index + 1, [])
    }
